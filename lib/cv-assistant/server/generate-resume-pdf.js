async function renderHybrid(doc, profile, regular, bold) {
  const first = newPage(doc)
  let state = {
    doc,
    page: first.page,
    y: first.height - PAGE_MARGIN,
    width: first.width,
    height: first.height,
    regular,
    bold,
  }

  state = drawHeader(doc, state, profile, buildContactLines(profile.personalInfo))
  state.y -= 6

  if (profile.professionalSummary || profile.target?.desiredRole) {
    state = drawHeading(state, 'Profile')
    if (profile.target?.desiredRole) {
      state = drawParagraph(state, `Target: ${profile.target.desiredRole}`, {
        size: 10,
        color: rgb(0.27, 0.32, 0.4),
      })
    }
    if (profile.professionalSummary) {
      state = drawParagraph(state, profile.professionalSummary, { size: 11 })
    }
  }

  if (profile.skills.length > 0) {
    state = drawHeading(state, 'Core Skills')
    state = drawSkillGroups(doc, state, profile.skills)
  }

  if (profile.experience.length > 0) {
    state = drawHeading(state, 'Selected Achievements & Relevant Experience')
    state = drawExperience(doc, state, profile.experience, { dates: true })
  }

  if (profile.projects.length > 0) {
    state = drawHeading(state, 'Projects')
    state = drawProjects(doc, state, profile.projects)
  }

  if (profile.education.length > 0) {
    state = drawHeading(state, 'Education')
    state = drawEducation(doc, state, profile.education)
  }

  return doc.save()
}
