import { PDFDocument, StandardFonts, rgb } from 'pdf-lib'
import { evaluateTemplate, validateGlobalMinimum } from '../validation.js'
import { getTemplate } from '../template-catalog.js'

export class PDFGenerationError extends Error {
  constructor(message, code) {
    super(message)
    this.code = code
  }
}

const PAGE_MARGIN = 48

export async function generateResumePDF(input) {
  const global = validateGlobalMinimum(input.profile)
  if (!global.isValid) {
    throw new PDFGenerationError(
      `Profile is incomplete: ${global.missingRequiredFields.join(', ')}.`,
      'PROFILE_INCOMPLETE',
    )
  }

  const template = getTemplate(input.templateKey)
  const evaluation = evaluateTemplate(input.profile, template)
  if (evaluation.status === 'needs_more_information') {
    throw new PDFGenerationError(
      `Template requirements not met: ${evaluation.missingRequiredFields.join(', ')}.`,
      'TEMPLATE_REQUIREMENTS_NOT_MET',
    )
  }

  let pdfBytes
  try {
    pdfBytes = await renderTemplate(input.profile, template)
  } catch (err) {
    throw new PDFGenerationError(
      err instanceof Error ? err.message : 'PDF rendering failed.',
      'PDF_GENERATION_FAILED',
    )
  }

  const fileName = buildFileName(input.profile, template)
  return { pdfBytes, fileName, template }
}

function buildFileName(profile, template) {
  const slug = profile.personalInfo.fullName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
  return `${slug || 'resume'}-${template.key}.pdf`
}

async function renderTemplate(profile, template) {
  const doc = await PDFDocument.create()
  doc.setTitle(`${profile.personalInfo.fullName} - ${template.name}`)
  doc.setAuthor(profile.personalInfo.fullName)
  doc.setSubject('CV generated by Career Forge')
  doc.setProducer('Career Forge CV Assistant')
  doc.setCreator('Career Forge CV Assistant')

  const regular = await doc.embedFont(StandardFonts.Helvetica)
  const bold = await doc.embedFont(StandardFonts.HelveticaBold)

  switch (template.key) {
    case 'harvard-classic':
      return renderHarvardLike(doc, profile, regular, bold)
    case 'ats-simple':
      return renderAtsSimple(doc, profile, regular, bold)
    case 'reverse-chronological-professional':
      return renderReverseChronological(doc, profile, regular, bold)
    case 'technical-projects':
      return renderTechnicalProjects(doc, profile, regular, bold)
    case 'hybrid-combination':
      return renderHybrid(doc, profile, regular, bold)
  }
}

function newPage(doc) {
  const page = doc.addPage([612, 792])
  return { page, width: page.getWidth(), height: page.getHeight() }
}

function ensureSpace(doc, state, needed) {
  if (state.y - needed < PAGE_MARGIN) {
    const { page, width, height } = newPage(doc)
    return { ...state, page, y: height - PAGE_MARGIN, width, height }
  }
  return state
}

function drawHeading(state, text) {
  state = ensureSpace(state.doc, state, 24)
  state.page.drawText(text.toUpperCase(), {
    x: PAGE_MARGIN,
    y: state.y,
    size: 12,
    font: state.bold,
    color: rgb(0.06, 0.07, 0.13),
  })
  state.y -= 6
  state.page.drawLine({
    start: { x: PAGE_MARGIN, y: state.y },
    end: { x: state.width - PAGE_MARGIN, y: state.y },
    thickness: 0.6,
    color: rgb(0.8, 0.84, 0.9),
  })
  state.y -= 14
  return state
}

function drawParagraph(state, text, opts) {
  const size = opts?.size ?? 10
  const font = opts?.font ?? state.regular
  const color = opts?.color ?? rgb(0.12, 0.16, 0.23)
  const lines = wrapText(text, font, size, state.width - PAGE_MARGIN * 2)
  for (const line of lines) {
    state = ensureSpace(state.doc, state, size + 4)
    state.page.drawText(line, {
      x: PAGE_MARGIN,
      y: state.y,
      size,
      font,
      color,
    })
    state.y -= size + 3
  }
  return state
}

function drawBullet(state, text) {
  const lines = wrapText(text, state.regular, 10, state.width - PAGE_MARGIN * 2 - 14)
  state = ensureSpace(state.doc, state, 12)
  state.page.drawText('•', {
    x: PAGE_MARGIN,
    y: state.y,
    size: 10,
    font: state.regular,
    color: rgb(0.12, 0.16, 0.23),
  })
  let first = true
  for (const line of lines) {
    state = ensureSpace(state.doc, state, 12)
    state.page.drawText(line, {
      x: PAGE_MARGIN + 12,
      y: state.y,
      size: 10,
      font: state.regular,
      color: rgb(0.12, 0.16, 0.23),
    })
    state.y -= 12
    first = false
  }
  return state
}

function wrapText(text, font, size, maxWidth) {
  const words = text.split(/\s+/)
  const lines = []
  let current = ''
  for (const word of words) {
    const candidate = current ? `${current} ${word}` : word
    const width = font.widthOfTextAtSize(candidate, size)
    if (width > maxWidth && current) {
      lines.push(current)
      current = word
    } else {
      current = candidate
    }
  }
  if (current) lines.push(current)
  return lines
}

function drawHeader(doc, state, profile, contactLines) {
  const nameSize = 22
  state.page.drawText(profile.personalInfo.fullName, {
    x: PAGE_MARGIN,
    y: state.y,
    size: nameSize,
    font: state.bold,
    color: rgb(0.06, 0.07, 0.13),
  })
  state.y -= nameSize + 4

  if (profile.personalInfo.headline) {
    state = drawParagraph(state, profile.personalInfo.headline, {
      size: 11,
      color: rgb(0.29, 0.34, 0.4),
    })
  }

  for (const line of contactLines) {
    state = drawParagraph(state, line, {
      size: 9,
      color: rgb(0.27, 0.32, 0.4),
    })
  }
  state.y -= 4
  return state
}

function buildContactLine(info) {
  const parts = []
  if (info.email) parts.push(info.email)
  if (info.phone) parts.push(info.phone)
  if (info.location) parts.push(info.location)
  return parts.join('  ·  ')
}

function buildContactLines(info) {
  const lines = []
  const primary = buildContactLine(info)
  if (primary) lines.push(primary)
  const links = []
  if (info.linkedinUrl) links.push(info.linkedinUrl)
  if (info.githubUrl) links.push(info.githubUrl)
  if (info.portfolioUrl) links.push(info.portfolioUrl)
  if (info.websiteUrl) links.push(info.websiteUrl)
  if (links.length > 0) lines.push(links.join('  ·  '))
  return lines
}

function drawExperience(doc, state, experience, options = {}) {
  for (const item of experience) {
    state = ensureSpace(doc, state, 28)
    const title = `${item.position} — ${item.company}`
    state.page.drawText(title, {
      x: PAGE_MARGIN,
      y: state.y,
      size: 11,
      font: state.bold,
      color: rgb(0.06, 0.07, 0.13),
    })
    state.y -= 14
    const meta = []
    if (item.location) meta.push(item.location)
    if (options.dates) {
      const start = item.startDate ?? ''
      const end = item.isCurrent ? 'Present' : item.endDate ?? ''
      if (start || end) meta.push([start, end].filter(Boolean).join(' — '))
    }
    if (meta.length > 0) {
      state = drawParagraph(state, meta.join('  ·  '), {
        size: 9,
        color: rgb(0.4, 0.45, 0.5),
      })
    }
    if (item.summary) {
      state = drawParagraph(state, item.summary, { size: 10 })
    }
    for (const highlight of item.highlights ?? []) {
      state = drawBullet(state, highlight)
    }
    if (item.technologies && item.technologies.length > 0) {
      state = drawParagraph(state, `Technologies: ${item.technologies.join(', ')}`, {
        size: 9,
        color: rgb(0.4, 0.45, 0.5),
      })
    }
    state.y -= 6
  }
  return state
}

function drawEducation(doc, state, education) {
  for (const item of education) {
    state = ensureSpace(doc, state, 20)
    const title = item.degree
      ? `${item.degree}${item.fieldOfStudy ? ` in ${item.fieldOfStudy}` : ''} — ${item.institution}`
      : item.institution
    state.page.drawText(title, {
      x: PAGE_MARGIN,
      y: state.y,
      size: 11,
      font: state.bold,
      color: rgb(0.06, 0.07, 0.13),
    })
    state.y -= 14
    const meta = []
    if (item.startDate || item.endDate) {
      meta.push(
        [item.startDate, item.isCurrent ? 'Present' : item.endDate]
          .filter(Boolean)
          .join(' — '),
      )
    }
    if (item.location) meta.push(item.location)
    if (meta.length > 0) {
      state = drawParagraph(state, meta.join('  ·  '), {
        size: 9,
        color: rgb(0.4, 0.45, 0.5),
      })
    }
    for (const highlight of item.highlights ?? []) {
      state = drawBullet(state, highlight)
    }
    state.y -= 4
  }
  return state
}

function drawSkillGroups(doc, state, skills) {
  for (const group of skills) {
    state = ensureSpace(doc, state, 18)
    const line = `${group.category}: ${group.items.join(', ')}`
    state = drawParagraph(state, line, { size: 10 })
    state.y -= 2
  }
  return state
}

function drawProjects(doc, state, projects) {
  for (const project of projects) {
    state = ensureSpace(doc, state, 22)
    state.page.drawText(project.name, {
      x: PAGE_MARGIN,
      y: state.y,
      size: 11,
      font: state.bold,
      color: rgb(0.06, 0.07, 0.13),
    })
    state.y -= 14
    if (project.role) {
      state = drawParagraph(state, project.role, {
        size: 9,
        color: rgb(0.4, 0.45, 0.5),
      })
    }
    if (project.description) {
      state = drawParagraph(state, project.description, { size: 10 })
    }
    for (const highlight of project.highlights ?? []) {
      state = drawBullet(state, highlight)
    }
    if (project.technologies && project.technologies.length > 0) {
      state = drawParagraph(state, `Stack: ${project.technologies.join(', ')}`, {
        size: 9,
        color: rgb(0.4, 0.45, 0.5),
      })
    }
    if (project.url || project.repositoryUrl) {
      const url = [project.url, project.repositoryUrl].filter(Boolean).join(' · ')
      state = drawParagraph(state, url, {
        size: 9,
        color: rgb(0.11, 0.31, 0.85),
      })
    }
    state.y -= 4
  }
  return state
}

function drawCertifications(doc, state, certs) {
  for (const cert of certs) {
    state = ensureSpace(doc, state, 14)
    const line = [cert.name, cert.issuer, cert.issueDate].filter(Boolean).join(' — ')
    state = drawParagraph(state, line, { size: 10 })
  }
  return state
}

function drawLanguages(doc, state, languages) {
  const line = languages
    .map((l) => (l.proficiency ? `${l.name} (${l.proficiency})` : l.name))
    .join('  ·  ')
  if (!line) return state
  return drawParagraph(state, line, { size: 10 })
}

async function renderHarvardLike(doc, profile, regular, bold) {
  const first = newPage(doc)
  let state = {
    doc,
    page: first.page,
    y: first.height - PAGE_MARGIN,
    width: first.width,
    height: first.height,
    regular,
    bold,
  }

  state = drawHeader(doc, state, profile, buildContactLines(profile.personalInfo))
  state.y -= 6

  if (profile.professionalSummary) {
    state = drawHeading(state, 'Summary')
    state = drawParagraph(state, profile.professionalSummary, { size: 10 })
    state.y -= 4
  }

  if (profile.education.length > 0) {
    state = drawHeading(state, 'Education')
    state = drawEducation(doc, state, profile.education)
  }

  if (profile.experience.length > 0) {
    state = drawHeading(state, 'Experience')
    state = drawExperience(doc, state, profile.experience, { dates: true })
  }

  if (profile.projects.length > 0) {
    state = drawHeading(state, 'Projects / Leadership')
    state = drawProjects(doc, state, profile.projects)
  }

  if (profile.skills.length > 0) {
    state = drawHeading(state, 'Skills')
    state = drawSkillGroups(doc, state, profile.skills)
  }

  if (profile.languages.length > 0) {
    state = drawHeading(state, 'Languages')
    state = drawLanguages(doc, state, profile.languages)
  }

  return doc.save()
}

